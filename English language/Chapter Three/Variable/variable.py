# First example - integer variables

a = 5
b = 10
c = a + b
print(c) # Output: 15

# Second example - string variables

name = "John"
message = "Hello, " + name + "!"
print(message) # Output: Hello, John!

# Third example - Boolean variables

is_raining = True
is_cold = False
print(is_raining) # Output: True
print(is_cold) # Output: False

# Fourth example - floating point variables

x = 3.14
y = 2.5
z = x * y
print(z) # Output: 7.85
